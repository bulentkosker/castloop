function setLang(lang) {
  localStorage.setItem('castreo_lang', lang);
  document.querySelectorAll('.lang-select').forEach(s => s.value = lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const t = (translations[lang] || translations.en)[key] || translations.en[key];
    if (t !== undefined) el.innerHTML = t;
  });
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
}

(function() {
  const lang = localStorage.getItem('castreo_lang') || 'en';
  document.querySelectorAll('.lang-select').forEach(s => {
    s.innerHTML = LANG_SELECT_HTML;
    s.value = lang;
    s.onchange = function() { setLang(this.value); };
  });
  setLang(lang);
})();
